package model;

public class User {

	
	
	private String userName;
	private String passWord;
	
	public User(String username, String password) {
		userName = username;
		passWord = password;
	}

	public String getUserName() {
		return userName;
	}

	public String getPassWord() {
		return passWord;
	}
	
	
	
	
	
	
}
